package com.whch.presentCloud.entity;

public class container2 {
    public String label;

    public String value;

    public container2() {
    }

    public container2(String label, String value) {
        this.label = label;
        this.value = value;
    }

    public String getLabel() {
        return label;
    }

    public void setLabel(String label) {
        this.label = label;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }
}
